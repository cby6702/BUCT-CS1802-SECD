package com.example.baidumapdemo.zouao.Bean;


public class MuseumItem {
    private int mid;
    private String name;
    private String introduction;
    private String opentime;
    private double lat;
    private String picture;
    private String city;
    private int exhibition_score;
    private int environment_score;
    private int service_score;
    private int general_score;
    private int annual_exhibitions;
    private String ing;


    public void setMid(int mid) {
        this.mid = mid;
    }
    public int getMid() {
        return mid;
    }

    public void setName(String name) {
        this.name = name;
    }
    public String getName() {
        return name;
    }

    public void setIntroduction(String introduction) {
        this.introduction = introduction;
    }
    public String getIntroduction() {
        return introduction;
    }

    public void setOpentime(String opentime) {
        this.opentime = opentime;
    }
    public String getOpentime() {
        return opentime;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }
    public double getLat() {
        return lat;
    }

    public void setPicture(String picture) {
        this.picture = picture;
    }
    public String getPicture() {
        return picture;
    }

    public void setCity(String city) {
        this.city = city;
    }
    public String getCity() {
        return city;
    }

    public void setExhibition_score(int exhibition_score) {
        this.exhibition_score = exhibition_score;
    }
    public int getExhibition_score() {
        return exhibition_score;
    }

    public void setEnvironment_score(int environment_score) {
        this.environment_score = environment_score;
    }
    public int getEnvironment_score() {
        return environment_score;
    }

    public void setService_score(int service_score) {
        this.service_score = service_score;
    }
    public int getService_score() {
        return service_score;
    }

    public void setGeneral_score(int general_score) {
        this.general_score = general_score;
    }
    public int getGeneral_score() {
        return general_score;
    }

    public void setAnnual_exhibitions(int annual_exhibitions) {
        this.annual_exhibitions = annual_exhibitions;
    }
    public int getAnnual_exhibitions() {
        return annual_exhibitions;
    }

    public void setIng(String ing) {
        this.ing = ing;
    }
    public String getIng() {
        return ing;
    }
}
